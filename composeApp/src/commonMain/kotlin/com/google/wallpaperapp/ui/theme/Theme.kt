@Composable
fun ScreenyTheme(
    dynamicColor: Boolean,
    darkTheme: Boolean,
    content: @Composable () -> Unit
) {

    val colorScheme = colorSchemeProvide(
        isDynamicColor = dynamicColor,
        isDarkMode = darkTheme
    )

    MaterialTheme(
        colorScheme = colorScheme,
        typography = getAppTypography(),
        content = content
    )
}